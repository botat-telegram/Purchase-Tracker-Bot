"""
محول الأرقام
يتيح تحويل الأرقام العربية والهندية إلى أرقام إنجليزية
"""

def convert_to_english_numbers(text: str) -> str:
    """
    تحويل الأرقام العربية والهندية في النص إلى أرقام إنجليزية

    Args:
        text: النص المراد تحويل أرقامه

    Returns:
        النص بعد تحويل الأرقام إلى الصيغة الإنجليزية
    """
    if not text:
        return text

    # قاموس تحويل الأرقام العربية إلى إنجليزية
    arabic_to_english = {
        # الأرقام العربية
        '٠': '0', '١': '1', '٢': '2', '٣': '3', '٤': '4',
        '٥': '5', '٦': '6', '٧': '7', '٨': '8', '٩': '9',
        # الأرقام الهندية
        '۰': '0', '۱': '1', '۲': '2', '۳': '3', '۴': '4',
        '۵': '5', '۶': '6', '۷': '7', '۸': '8', '۹': '9'
    }

    # استبدال كل رقم عربي بالمقابل الإنجليزي
    result = ""
    for char in text:
        result += arabic_to_english.get(char, char)

    return result


if __name__ == "__main__":
    # اختبار وظائف التحويل
    test_numbers = [
        "١٢٣٤٥٦٧٨٩٠",
        "۱۲۳۴۵۶۷۸۹۰",
        "مبلغ ١٠٠ ريال",
        "١٢.٥",
        "۲,۵۰۰",
        "123456789"
    ]

    for test in test_numbers:
        result = convert_to_english_numbers(test)
        print(f"الأصل: {test}")
        print(f"بعد التحويل: {result}")
        print("----------")
